  static int of(int codePoint) {
    if (codePoint == 0) return 0;
    if (codePoint < 32 || (codePoint >= 0x7f && codePoint < 0xa0)) return 0;
    if (_contains(_zeroWidth, codePoint)) return 0;
    if (_contains(_wide, codePoint)) return 2;
    return 1;
  }

  static bool _contains(List<(int, int)> ranges, int codePoint) {
    var low = 0;
    var high = ranges.length - 1;
    while (low <= high) {
      final mid = (low + high) >> 1;
      final range = ranges[mid];
      if (codePoint < range.$1) {
        high = mid - 1;
      } else if (codePoint > range.$2) {
        low = mid + 1;
      } else {
        return true;
      }
    }
    return false;
  }

